package org.java.android.database;

public class Author {
	private int id_;
	private int authorId_;
	private String name_;
	private String password_;
	private String email_;
	/**
	 * @return the id_
	 */
	public int getId() {
		return id_;
	}
	/**
	 * @param id_ the id_ to set
	 */
	public void setId(int id_) {
		this.id_ = id_;
	}
	/**
	 * @return the authorId_
	 */
	public int getAuthorId() {
		return authorId_;
	}
	/**
	 * @param authorId_ the authorId_ to set
	 */
	public void setAuthorId(int authorId_) {
		this.authorId_ = authorId_;
	}
	/**
	 * @return the name_
	 */
	public String getName() {
		return name_;
	}
	/**
	 * @param name_ the name_ to set
	 */
	public void setName(String name_) {
		this.name_ = name_;
	}
	/**
	 * @return the password_
	 */
	public String getPassword() {
		return password_;
	}
	/**
	 * @param password_ the password_ to set
	 */
	public void setPassword(String password_) {
		this.password_ = password_;
	}
	/**
	 * @return the email_
	 */
	public String getEmail() {
		return email_;
	}
	/**
	 * @param email_ the email_ to set
	 */
	public void setEmail(String email_) {
		this.email_ = email_;
	}
}
