package org.java.android.database;

public class Comment {
	private int id_;
	private String commentBody_;
	private String date_;
	/**
	 * @return the id_
	 */
	public int getId() {
		return id_;
	}
	/**
	 * @param id_ the id_ to set
	 */
	public void setId(int id_) {
		this.id_ = id_;
	}
	/**
	 * @return the commentBody_
	 */
	public String getCommentBody() {
		return commentBody_;
	}
	/**
	 * @param commentBody_ the commentBody_ to set
	 */
	public void setCommentBody(String commentBody_) {
		this.commentBody_ = commentBody_;
	}
	/**
	 * @return the date_
	 */
	public String getDate() {
		return date_;
	}
	/**
	 * @param date_ the date_ to set
	 */
	public void setDate(String date_) {
		this.date_ = date_;
	}
}
